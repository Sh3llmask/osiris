msg4 = "                	[4F 73 69 72 69 73]\n"
def bannerChoice():
	banners = [banner1]
	msg = [msg1, msg2, msg3, msg4]
	bannerMSG = random.choice(banners) + random.choice(msg)
	clear = "\x1b[0m"
	colors = [31, 32, 33, 34, 35, 36]
	logo = bannerMSG
	for line in logo.split("\n"):
		sys.stdout.write("\x1b[1;%dm%s%s\n" % (random.choice(colors), line, clear))
		time.sleep(0.05)
